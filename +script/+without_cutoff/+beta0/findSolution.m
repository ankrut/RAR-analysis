function varargout = findSolution(varargin)
searchCfg = lib.require(@configs.solutionSearchConfig);

Q = lib.module.struct(...
	'nlinfit',			{},...
	'gosect',			{},...
	'ResponseList',		searchCfg.ResponseList,...
	varargin{:}...
);

% set nlinfit options (for autofind)
opts				= statset('nlinfit');
opts.FunValCheck	= 'off';
opts.MaxIter		= 50;
% opts.TolX			= 1E-12;

fSolution = {};

fSolution{end+1} = @(vm,list) script.nlinfit.beta0(...
	'model',	lib.struct.setfield(vm,'param.beta0',0.9*vm.param.beta0),...
	'list',		list,...
	'nlinfit',	{'options', opts, Q.nlinfit{:}} ...
);

fSolution{end+1} = @(vm,list) script.gosect.beta0(...
	'model',	vm,...
	'list',		list,...
	'dbeta0',	0.3*vm.param.beta0,...
	'gosect',	Q.gosect ...
);

[varargout{1:nargout}] = lib.module.find(...
	'query',		Q.query,...
	'ResponseList',	Q.ResponseList,...
	'model',		Q.model,...
	'fSolution',	fSolution ...
);