function fh = figure()

BETA0LIM	= [1E-6,1E2];
RLIM		= [1E0,1E10];
MLIM		= [1E0,1E9];
DTHETALIM	= [-5,55];

BETA0TICK	= 10.^(-5:2:1);

% axes model
AXMODEL.radius = {...
	'x','$\beta_0$',...
	'y','$r/r_c$',...
	'axes', {...
		'XLim',		BETA0LIM, ...
		'YLim',		RLIM,...
		'XTick',	BETA0TICK,...
		'YTick',	10.^(0:2:10),...
		'XScale',	'log',...
		'YScale',	'log' ...
	}...
};

AXMODEL.mass = {...
	'x', '$\beta_0$',...
	'y', '$M(r)/M_c$',...
	'axes',	{...
		'XLim',		BETA0LIM, ...
		'YLim',		MLIM,...
		'XTick',	BETA0TICK,...
		'YTick',	10.^(0:2:8),...
		'XScale',	'log',...
		'YScale',	'log' ...
	}...
};

AXMODEL.degeneracy = {...
	'x','$\beta_0$',...
	'y','$\theta_0 - \theta(r)$',...
	'axes',{...
		'XLim',		BETA0LIM, ...
		'YLim',		DTHETALIM,...
		'XTick',	BETA0TICK,...
		'YTick',	0:10:50,...
		'XScale',	'log' ...
	}...
};

% create axes grid
for ii=1:3
	ax(:,ii) = [
		lib.module.figure.axes(AXMODEL.degeneracy{:})
		lib.module.figure.axes(AXMODEL.mass{:})
		lib.module.figure.axes(AXMODEL.radius{:})
	];
end

% figure
fh = lib.module.sapthesis.figure_grid(ax,'axHeight','equal');